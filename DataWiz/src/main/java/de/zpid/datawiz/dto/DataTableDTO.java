public class DataTableDTO implements Serializable {


    private static final long serialVersionUID = -1486863136510024230L;
    private int draw;
    private int recordsTotal;
    private int recordsFiltered;
    private List<List<Object>> data;
    private String error;

    public int getDraw() {
        return draw;
    }

    public void setDraw(int draw) {
        this.draw = draw;
    }

    public int getRecordsTotal() {
        return recordsTotal;
    }

    public void setRecordsTotal(int recordsTotal) {
        this.recordsTotal = recordsTotal;
    }

    public int getRecordsFiltered() {
        return recordsFiltered;
    }

    public void setRecordsFiltered(int recordsFiltered) {
        this.recordsFiltered = recordsFiltered;
    }

    public List<List<Object>> getData() {
        return data;
    }

    public void setData(List<List<Object>> data) {
        this.data = data;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        DataTableDTO that = (DataTableDTO) o;
        return draw == that.draw &&
                recordsTotal == that.recordsTotal &&
                recordsFiltered == that.recordsFiltered &&
                Objects.equals(data, that.data) &&
                Objects.equals(error, that.error);
    }

    @Override
    public int hashCode() {

        return Objects.hash(draw, recordsTotal, recordsFiltered, data, error);
    }

    @Override
    public String toString() {
        return "DataTableDTO{" +
                "draw=" + draw +
                ", recordsTotal=" + recordsTotal +
                ", recordsFiltered=" + recordsFiltered +
                ", data=" + data +
                ", error='" + error + '\'' +
                '}';
    }
}
